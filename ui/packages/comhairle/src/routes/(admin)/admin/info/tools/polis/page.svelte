	<h1>Polis</h1>

	<h2>What is Polis</h2>

	<p>
		Polis is an open-source digital tool designed to facilitate large-scale, inclusive, and
		constructive public deliberation. Unlike traditional surveys or comment threads, Polis allows
		participants to contribute their own statements and vote on the statements of others with a
		simple “agree,” “disagree,” or “pass.” This structure minimizes unproductive back-and-forth
		exchanges and instead highlights areas of consensus and disagreement. By combining machine
		learning with participatory design, Polis can map complex discussions into visual clusters of
		opinion, making it easier for both participants and decision-makers to understand the full range
		of perspectives in a community.
	</p>

	<p>
		One of Polis’s unique strengths is its ability to surface “emergent consensus”—points of
		agreement that might otherwise be overlooked in polarized debates. Participants often find
		common ground through the voting process, and these areas of agreement are elevated in real time
		as the conversation evolves. Policymakers, civic organizations, and even companies have used
		Polis to inform decision-making processes, ensuring that diverse voices are heard and that
		outcomes are based on collective input rather than top-down control.
	</p>

	<p>
		In practice, Polis has been used around the world in projects such as Taiwan’s vTaiwan platform,
		where it helped guide national policy on complex issues like ride-sharing regulation and online
		alcohol sales. Its design emphasizes both scale and civility: thousands of people can
		participate at once without discussions devolving into flame wars. By giving participants equal
		footing to contribute and by visualizing the broader shape of public opinion, Polis fosters more
		democratic, transparent, and legitimate decision-making in both governmental and organizational
		contexts.
	</p>

	<h2>When to use this tool</h2>
	<ul>
		<li>
			<strong>Large-scale participation</strong> — Polis can handle thousands of participants at once,
			making it suitable when you want broad input beyond what’s possible in town halls, focus groups,
			or small assemblies.
		</li>
		<li>
			<strong>Mapping opinion landscapes</strong> — Its clustering algorithm reveals patterns of agreement
			and disagreement, helping identify areas of consensus and polarization more clearly than open-ended
			discussions.
		</li>
		<li>
			<strong>Reducing domination</strong> — Since participants submit short statements and vote anonymously
			on others’ contributions, no single voice can dominate, unlike in face-to-face deliberation.
		</li>
		<li>
			<strong>Real-time feedback</strong> — Policymakers or organizers can see evolving consensus clusters
			and test how framing or proposals shift support, something traditional deliberative formats often
			lack.
		</li>
	</ul>

	<h2>Interpreting the results</h2>
	<p>
		Interpreting the results from a Polis conversation requires looking at both the individual
		statements and the broader opinion groups that form through participant voting. As people vote
		on each other’s contributions, Polis uses statistical clustering to identify groups of
		participants who share similar patterns of agreement and disagreement. These clusters are
		visualized in a two-dimensional map, making it easier to see how different groups are positioned
		relative to one another and where polarization exists. Rather than reducing results to a single
		average opinion, Polis highlights the diversity of perspectives within a community.
	</p>
	<p>
		A key feature of Polis results is the identification of “consensus statements.” These are
		contributions that received broad support across different clusters, even among groups that
		disagree on many other issues. Consensus statements are particularly valuable because they
		reveal areas of shared ground that can serve as a foundation for policy decisions or next steps.
		On the other hand, statements with strong support in one group but rejection in another can help
		decision-makers understand where divisions lie and why certain compromises may be more
		difficult.
	</p>
	<p>
		Ultimately, interpreting Polis results means paying attention to both the agreements and the
		disagreements. The consensus highlights what unites participants and points toward actionable
		common ground, while the polarized statements clarify the contours of debate and the values at
		stake. By considering both dimensions, policymakers, organizations, or facilitators can use
		Polis outcomes not just as raw feedback, but as a roadmap for building more legitimate and
		widely accepted solutions.
	</p>
