	<h2>Directorate for Communications and Ministerial Support</h2>
	<h3>Who we are</h3>
	<p>
		We are the Digital Engagement Team, within the Communications and Ministerial Support
		Directorate of Scottish Government. Our head office is located at St. Andrew’s House, 2 Regent
		Road, Edinburgh, EH1 3DG.
	</p>
	<h2>What personal information do we collect and why do we need it?</h2>
	<p>
		We use the website Comhairle, which includes different tools called HeyForms and Polis to help
		us run and manage the engagement.
	</p>
	<p>
		Comhairle is where you will set up your user account, HeyForms is where we will collect your
		question responses and Polis will be used for gathering and analysing the information you
		provide in your responses.
	</p>
	<p>Listed below what each area of the Comhairle website is looking to collect from you:</p>

	<h2>Comhairle Website</h2>
	<p>Comhairle provides the option to register a user account using your email address.</p>
	<p>
		If you decide to create an account, we will collect the following information from you through
		an online form:
	</p>
	<ul>
		<li>
			Username: you can create your own username to personalise your greetings and correspondence
		</li>
		<li>Password: you can create your own password</li>
		<li>Email Address: you can choose which address you want to be held for the account</li>
	</ul>
	<p>
		Creating a password and providing an email address will allow you to verify your account and to
		return/log into your account at any time.
	</p>
	<p>
		It will ingalso allow you to be issued with critical emails for your account such as
		verification purposes. You may choose to receive optional notifications and updates about future
		engagement activities from Scottish Government on Comhairle which your email address will be
		used to communicate.
	</p>
	<p>
		Alternatingively, you can create an anonymous account which will provide you with a 7-digit code
		which you can use to log back in.
	</p>
	<p>
		Once youing have selected how you want to register an account on Comhairle, you will be able to
		take part in engagement activities on Hey Forms and Polis.
	</p>
	<h2>HeyForms</h2>
	<p>
		You will be asked to answer a set of questions which you can respond to in a free text box,
		selecting from multiple choice options and other formats.
	</p>
	<p>
		You may be asked to provide additional information about yourself such as age, gender, and
		location. You may also be asked to provide more sensitive information about yourself on your
		health, religious or philosophical beliefs, racial or ethnic origin, sexual orientation, and
		political opinions which fall under special category data.
	</p>
	<p>
		You can decide what information you provide; you are not required to provide the information
		unless you wish to do so.
	</p>
	<p>
		We ask for this information to help understand who the Scottish Government are reaching when
		each activity is set up and to see how opinions may be shared among people that have similar
		characteristics.
	</p>
	<h3>Polis</h3>
	<p>
		Polis will create a prompt and ask you to vote on whether you agree or disagree with the
		statement, or you have the option to pass. You also have the option to respond to the prompt
		with a statement however, you can choose not to submit statements.
	</p>
	<p>
		Polis will also ask you to vote on the statements made by others taking part in the same
		activity. The information you provide in Polis will be used to support analysis of public
		opinion in relation to the different statements.
	</p>
	<p>
		The responses you provide will link to your account on Comhairle (both personalised and
		anonymised) through a unique identifier to record who has taken part in the engagement activity.
	</p>
	<h2>What is our lawful basis?</h2>

	<p>
		The lawful basis we rely on to process your personal information for the purpose of registering
		an account on Comhairle and taking part in engagement activity on HeyForms and Polis is Article
		6(1)(e) of the UK GDPR is ‘Processing is necessary for you to perform a task in the public
		interest or for your official functions, and the task or function has a clear basis in law’.
	</p>
	<p>
		The lawful basis being relied on to process special category personal data is Article 9(a) ‘The
		data subject has given consent to the processing of their personal data for one or more specific
		purposes’.
	</p>
	<p>
		In providing this data you are consenting to its use for the purpose outlined at the point of
		collection. You will never be required to provide this data and can take part in other elements
		of the engagement activity regardless of whether you have provided this data or not.
	</p>
	<h2>What we do with your personal information?</h2>
	<p>
		The information you provide when using Comhairle, Polis, and/or HeyForms will be stored within
		Comhairle which is hosted by CrownShy.
	</p>
	<p>Further information is available <a href="/rights/privacy/">Comhairle – Privacy Notice.</a></p>
	<p>
		If you choose to create a personal account your email address, username and password will be
		stored in Comhairle and used to maintain and for you to access your account.
	</p>
	<p>
		If you choose to, your email address will also be used to allow you to receive updates about the
		engagement activities you have taken part in, as well as updates about new activities from
		Scottish Government you might be interested in.
	</p>
	<p>
		The responses you provide into HeyForms and Polis will be analysed by the Scottish Government
		and CrownShy to report to and inform policy teams within the Scottish Government.
	</p>
	<p>
		As part of the analysis and to help find common themes and categories in the free text responses
		– CrownShy will be using a language learning model. This model will group together comments
		which share certain phrases or sentiment and summarise these comments for officials to read.
	</p>
	<p>
		No personal data will be included in the final reports with the responses you provide being
		presented as anonymous quotes or combined with other responses to form statistics before being
		shared.
	</p>
	<p>The finalised reports may be published but will not contain personal information.</p>
	<h3>Polis</h3>
	<p>
		When you submit statements into Polis, these will be presented anonymously to other people
		taking part in the same activity and may be anonymously quoted in final reports.
	</p>
	<p>
		The statements you provide will be moderated by Scottish Government and CrownShy personnel
		before they are shared with other people participating in the activity to ensure no personal
		information is shared and that they are within the moderation policy.
	</p>
	<p>
		The votes you submit will inform a set of rules which will prioritise the statements you will
		see next. The set of rules will only look at your voting pattern and not the content of any
		statements you provide.
	</p>
	<p>CrownShy will use the data collected about voting patterns to</p>
	<ul>
		<li>
			generate statistics on the prompt statement itself on whether it creates a consensus or
			divides opinion
		</li>
		<li>group statements to show where similar opinions lie</li>
		<li>
			generate statistics determining whether reactions to statements vary by demographics (only
			applicable if you have provided this data and consented to this use)
		</li>
	</ul>
	<h2>Cookies</h2>
	<p>
		When you visit the Comhairle website you will be able to choose if you would like to enable
		cookies. If the cookies are enabled, they will not track your activity but are used to remember
		your login details and language selections only.
	</p>
	<h2>How do we keep your personal information secure and how long is kept for?</h2>
	<p>Your information is securely stored.</p>
	<p>
		Your personal data is held within a secure database which is only accessible to a limited number
		of individuals who have permission to access based on their roles.
	</p>
	<p>
		Your username and email address are stored as plain text, with your password stored as a hashed
		version so it is not visible to others. You can change your username or password at any time.
		These will be stored as long as you have an active account on Comhairle.
	</p>
	<p>
		Your account would be treated as inactive if not used within 2 years. When the account becomes
		inactive, your personal information would be removed, and the account would become anonymous.
		Your response data would be kept in line with HeyForms and Polis timelines but would not be
		linked with you personally or your personal data.
	</p>
	<p>
		When you submit information to HeyForms or Polis it will be provided with a unique identifier
		which will link to your account on Comhairle to allow all your response data to link with you.
	</p>
	<p>
		Response data you submit will be kept for 1 year after the end of the activity you engage with.
	</p>
	<h2>Who can access your personal information?</h2>
	<p>Your password, email and username are only accessible to yourself and:</p>
	<ul>
		<li>the CrownShy team who own Comhairle for the purpose of system or account maintenance</li>
		<li>
			the Scottish Government for the purpose of complying with your rights as a data subject or to
			contact you in future if you have requested this
		</li>
	</ul>

	<p>The response data you submit via Polis and HeyForms may be accessible to:</p>
	<ul>
		<li>the CrownShy team who own Comhairle for the purpose of moderation and data analysis</li>
		<li>the Scottish Government for the purpose of moderation and data analysis.</li>
	</ul>
	<p>Your personal data will not be shared beyond those listed above.</p>
