	<h2>A note on reading comprehension</h2>
	<p>
		When writing for a reading age of 7–11 years, clarity and simplicity are the most important
		goals. People who read at this level can understand everyday words and sentences, but they may
		find long or complex writing difficult. This means it helps to use short, clear sentences and
		familiar vocabulary. Writers should avoid complicated structures or specialist terms unless they
		are explained in a straightforward way. The aim is to make the text easy to follow without
		losing meaning. Tone and style should be friendly and direct. Writing that speaks to the reader
		in a clear, conversational way is often the most effective. Descriptions and examples work well
		when they connect to everyday experiences, such as what something looks, feels, or sounds like.
		This makes ideas easier to picture and understand. Content should be accessible and relatable.
		Abstract concepts can be included, but they should be broken down into simple steps or supported
		with concrete examples. Visuals—like diagrams, pictures, or headings—can also help readers
		process information more easily. Above all, writing for this level should strike a balance:
		simple enough to be clear, but not so basic that it feels patronising. The goal is to create
		writing that builds confidence, helps the reader engage with the content, and makes the
		information feel approachable.
	</p>

	<h2>When to use this tool</h2>
